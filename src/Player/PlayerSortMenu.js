import React from 'react';
import Button from '@material-ui/core/Button';
import Menu from '@material-ui/core/Menu';
import MenuItem from '@material-ui/core/MenuItem';

export default function SimpleMenu() {
  const [anchorEl, setAnchorEl] = React.useState(null);

  const handleClick = event => {
    setAnchorEl(event.currentTarget);
  };

  const handleClose = () => {
    setAnchorEl(null);
  };

  return (
    <div>
      <Button aria-controls="simple-menu" aria-haspopup="true" onClick={handleClick}>
        Open Menu
      </Button>
      <Menu
        id="simple-menu"
        anchorEl={anchorEl}
        keepMounted
        open={Boolean(anchorEl)}
        onClose={handleClose}
      >
        <MenuItem onClick={handleClose}>Sort by Initiative</MenuItem>
        <MenuItem onClick={handleClose}>Sort by Remaining HP</MenuItem>
        <MenuItem onClick={handleClose}>Sort by Armor Class</MenuItem>
        <MenuItem onClick={handleClose}>Sort by Total Damage</MenuItem>
        <MenuItem onClick={handleClose}>Sort by Total HP</MenuItem>
      </Menu>
    </div>
  );
}
